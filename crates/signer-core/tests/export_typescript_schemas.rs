#[test]
fn signer_core_typescript_schemas_are_current() {
    let generated = generated_signer_core_commands_ts();
    let path = generated_schema_path();

    if env::var(UPDATE_ENV).as_deref() == Ok("1") {
        fs::create_dir_all(path.parent().expect("generated file has parent directory"))
            .expect("create generated schema directory");
        fs::write(&path, generated).expect("write generated schema file");
        return;
    }

    let existing = fs::read_to_string(&path).unwrap_or_else(|error| {
        panic!(
            "failed to read generated signer-core schema file at {}: {error}",
            path.display()
        )
    });

    assert_eq!(
        existing, generated,
        "generated signer-core schemas are stale; run `pnpm generate:signer-core-types`"
    );
}
