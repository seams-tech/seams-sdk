const GENERATED_HEADER: &str = concat!(
    "// This file was generated by `pnpm generate:signer-core-types`.\n",
    "// Do not edit this file manually.\n\n",
);

fn repo_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(|path| path.parent())
        .expect("signer-core crate is under crates/signer-core")
        .to_path_buf()
}

fn generated_schema_path() -> PathBuf {
    repo_root().join("client/src/core/platform/generated/signerCoreCommands.ts")
}

fn push_decl<T: TS + 'static>(out: &mut String, cfg: &Config) {
    out.push_str("export ");
    out.push_str(&T::decl(cfg));
    out.push_str("\n\n");
}

